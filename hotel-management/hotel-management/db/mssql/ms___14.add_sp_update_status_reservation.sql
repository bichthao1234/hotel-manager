CREATE PROCEDURE [dbo].[SP_UPDATE_STATUS_RESERVATION]
AS
BEGIN
    UPDATE dbo.PHIEU_DAT
    SET TRANG_THAI = 3
    WHERE GETDATE() > NGAY_BD_THUE and GETDATE() <= NGAY_DI and TRANG_THAI <> 2;
	UPDATE dbo.PHIEU_DAT
    SET TRANG_THAI = 0
    WHERE GETDATE() > NGAY_DI and TRANG_THAI <> 2;
END;
